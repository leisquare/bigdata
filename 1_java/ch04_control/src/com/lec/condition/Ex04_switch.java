package com.lec.condition;

public class Ex04_switch {
	public static void main(String[] args) {
		int num =2;
		switch(num) {
		case 1:
			System.out.println("林荤困传 1"); break;
		case 2:
			System.out.println("林荤困传 2"); break;
		case 3:
			System.out.println("林荤困传 3"); break;
		case 4:
			System.out.println("林荤困传 4"); break;
		case 5:
			System.out.println("林荤困传 5"); break;
		default:
			System.out.println("林荤困传 6"); break;
					
		}
	}
}
